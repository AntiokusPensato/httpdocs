// Variable without any keyword
name = "Trevor Davis";
alert("Variable without keyword: " + name);

// Variable with var keyword
var age = 36;
alert("Variable with var - " + "Age: " + age);

// Variable with let keyword
let homeState = "Missouri";
alert("Variable with let - " + "Home State: " + homeState);

//Variable with const keyword
const PI = 3.14159;
alert("Variable with const - " + "PI = " + PI);